public class even_or_odd 
{
    static void even_or_odd(int num){

        if (num % 2 == 0)
        {
            System.out.println("even");
        }
        else
        {
            System.out.println("odd");
        }

    }
    public static void main(String[] args)
    {
        even_or_odd(6);
    }
}
